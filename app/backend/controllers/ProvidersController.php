        if($request->isAjax){
            /*
            *   Process for ajax request
            */
            Yii::$app->response->format = Response::FORMAT_JSON;
            if($request->isGet){
                return [
                    'title'=> "Update Providers #".$id,
                    'content'=>$this->renderAjax('update', [
                        'model' => $model,
                    ]),
                    'footer'=> Html::button('Close',['class'=>'btn btn-default pull-left','data-dismiss'=>"modal"]).
                                Html::button('Save',['class'=>'btn btn-primary','type'=>"submit"])
                ];         
            }else if($model->load($request->post()) && $model->save()){
                return [
                    'forceReload'=>'#crud-datatable-pjax',
                    'title'=> "Providers #".$id,
                    'content'=>$this->renderAjax('view', [
                        'model' => $model,
                    ]),
                    'footer'=> Html::button('Close',['class'=>'btn btn-default pull-left','data-dismiss'=>"modal"]).
                            Html::a('Edit',['update','id'=>$id],['class'=>'btn btn-primary','role'=>'modal-remote'])
                ];    
            }else{
                 return [
                    'title'=> "Update Providers #".$id,
                    'content'=>$this->renderAjax('update', [
                        'model' => $model,
                    ]),
                    'footer'=> Html::button('Close',['class'=>'btn btn-default pull-left','data-dismiss'=>"modal"]).
                                Html::button('Save',['class'=>'btn btn-primary','type'=>"submit"])
                ];        
            }
        }else{
            /*
            *   Process for non-ajax request
            */
            if ($model->load($request->post()) && $model->save()) {
                return $this->redirect(['view', 'id' => $model->id]);
            } else {
                return $this->render('update', [
                    'model' => $model,
                ]);
            }
        }
    }

    /**
     * Delete an existing Providers model.
     * For ajax request will return json object
     * and for non-ajax request if deletion is successful, the browser will be redirected to the 'index' page.
     * @param integer $id
     * @return mixed
     */
    public function actionDelete($id)
    {
        $request = Yii::$app->request;
        $this->findModel($id)->delete();

        if($request->isAjax){
            /*
            *   Process for ajax request
            */
            Yii::$app->response->format = Response::FORMAT_JSON;
            return ['forceClose'=>true,'forceReload'=>'#crud-datatable-pjax'];
        }else{
            /*
            *   Process for non-ajax request
            */
            return $this->redirect(['index']);
        }


    }

     /**
     * Delete multiple existing Providers model.
     * For ajax request will return json object
     * and for non-ajax request if deletion is successful, the browser will be redirected to the 'index' page.
     * @param integer $id
     * @return mixed
     */
    public function actionBulkDelete()
    {        
        $request = Yii::$app->request;
        $pks = explode(',', $request->post( 'pks' )); // Array or selected records primary keys
        foreach ( $pks as $pk ) {
            $model = $this->findModel($pk);
            $model->delete();
        }

        if($request->isAjax){
            /*
            *   Process for ajax request
            */
            Yii::$app->response->format = Response::FORMAT_JSON;
            return ['forceClose'=>true,'forceReload'=>'#crud-datatable-pjax'];
        }else{
            /*
            *   Process for non-ajax request
            */
            return $this->redirect(['index']);
        }
       
    }

    /**
     * Finds the Providers model based on its primary key value.
     * If the model is not found, a 404 HTTP exception will be thrown.
     * @param integer $id
     * @return Providers the loaded model
     * @throws NotFoundHttpException if the model cannot be found
     */
    protected function findModel($id)
    {
        if (($model = Providers::findOne($id)) !== null) {
            return $model;
        } else {
            throw new NotFoundHttpException('The requested page does not exist.');
        }
    }

    /**
     * [actionExternalFormUrl description]
     * @param  [type] $id [description]
     * @return [type]     [description]
     */
    public function actionExternalFormUrl($id)
    {
        $validTime = ExternalProviderForm::getExpirationHashTime();
        
        $model = $this->findModel($id);
        // $type=isset($_POST['type']) ? $_POST['type'] : null;
        // FIXME si se entra mas de un IO???
        $type = 'Finance';
        //"providers_id=:prov AND status='Pending' AND type=:type", array(':prov'=>$id,':type'=>ucwords($type))
        $formURL = ExternalProviderForm::findOne(['status' => 'Pending', 'type' => 'Finance']);

        $url = 'http://kickadserver.mobi/';

        // $url = 'http://localhost/';

        if ( $formURL ) { // validate expiration hash's time
            if ( (time() - strtotime($formURL->create_date) ) > $validTime ) { // hash expired
                $formURL->create_date = date( 'Y-m-d H:i:s', time() );
                $formURL->type=ucwords($type);
                $formURL->hash = sha1($id . $model->name . $formURL->create_date);
                if ( ! $formURL->save() ) {
                    // echo "ERROR saving new hash <br>";
                    return;
                }
            } // else, hash not expired, do nothing
        } else { // Create new row for ExternalIoForm
            $formURL                 = new ExternalProviderForm;
            $formURL->create_date = date( 'Y-m-d H:i:s', time() );
            $formURL->providers_id = $id;
            $formURL->type=ucwords($type);
            // $formURL->commercial_id  = Yii::app()->user->id;
            $formURL->hash           = sha1($id . $model->name . $formURL->create_date);
            if ( ! $formURL->save() ) {
                // echo "ERROR saving new External IO Form <br>";
                return;
            }
        }
        $url   .= 'providers/financeDataEntry/' . $formURL->hash;
        
        return $this->render('generalDataEntry', [
                    'url' => $url,
                ]);
    }
}
